package com.business.astrology.util.ecliptic;

/**
 *  黄道状态
 * @author xp
 */
public class EclipticBean {
	
	public Integer star;		//星耀(星座)
	public String location;     //黄道位置
	public String speed;		//速度
	public Boolean rade;		//逆行
	public String temple;		//庙				
	public String wang;			//旺
	public String[] tpoints;		//三分
	public String circles;		//界
	public String noodles;		//面
	public String depression;	//陷
	public String weak;			//弱
	public String fraction;		//分数
	public Integer getStar() {
		return star;
	}
	public void setStar(Integer star) {
		this.star = star;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public Boolean getRade() {
		return rade;
	}
	public void setRade(Boolean rade) {
		this.rade = rade;
	}
	public String getTemple() {
		return temple;
	}
	public void setTemple(String temple) {
		this.temple = temple;
	}
	public String getWang() {
		return wang;
	}
	public void setWang(String wang) {
		this.wang = wang;
	}
	public String[] getTpoints() {
		return tpoints;
	}
	public void setTpoints(String[] tpoints) {
		this.tpoints = tpoints;
	}
	public String getCircles() {
		return circles;
	}
	public void setCircles(String circles) {
		this.circles = circles;
	}
	public String getNoodles() {
		return noodles;
	}
	public void setNoodles(String noodles) {
		this.noodles = noodles;
	}
	public String getDepression() {
		return depression;
	}
	public void setDepression(String depression) {
		this.depression = depression;
	}
	public String getWeak() {
		return weak;
	}
	public void setWeak(String weak) {
		this.weak = weak;
	}
	public String getFraction() {
		return fraction;
	}
	public void setFraction(String fraction) {
		this.fraction = fraction;
	}
	
}
